package dmc;

/**
 * A one dimensional walker.
 *
 * @author Ian Terrell
 */
public class Walker
{
    /**
     * The position of the walker.
     */
    public double x;

    /**
     * Constructor.  Sets x to the value given.
     *
     * @param x The value of x to initialize the Walker's position.
     */
    public Walker(double x)
    {
	this.x = x;
    }
}
